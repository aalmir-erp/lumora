"""E2E test: in-chat quote card flow.
1. Drive /api/chat to create a Q-XXXXXX (auto-generated by post-processor)
2. Verify reply contains [[quote_card: Q-XXX]] marker (not inline URLs)
3. Verify GET /api/q/<id>/card?session_id=<sid> returns full data
4. Verify POST /api/q/<id>/sign with session_id=<sid> stores signature
5. Verify the same quote then shows signed_at in /api/q/<id>/card
"""
import os, sys, json, types, re, uuid
os.environ["DB_PATH"] = "/tmp/test_quote_card_e2e.db"
os.environ["ANTHROPIC_API_KEY"] = "fake"
os.environ["DEMO_MODE"] = "off"
